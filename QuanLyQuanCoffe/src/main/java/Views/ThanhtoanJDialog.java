    private void loadTablehoaDon() {
        hoaDons = new ArrayList<>();
        List<HoaDon> hoaDon = new ArrayList<>();
        hoaDon = hoaDonService.getList();
        _Model = new DefaultTableModel();
        _Model = (DefaultTableModel) tbl_Donhang.getModel();
        _Model.setRowCount(0);
        for (int i = 0; i < hoaDon.size(); i++) {
            if (!hoaDon.get(i).isTinhTrangHD()) {
                hoaDons.add(hoaDon.get(i));
                int a = 0;
                hoaDonCTs = new ArrayList<>();
                hoaDonCTs = hoaDonCTService.getList(hoaDon.get(i));
                for (int j = 0; j < hoaDonCTs.size(); j++) {
                    a = a + hoaDonCTs.get(j).getGiaLM();
                }
                _Model.addRow(new Object[]{hoaDon.get(i).getKhachHang().getTenKH(), "chưa thanh toán", new SimpleDateFormat("dd/MM/yyyy").format(hoaDon.get(i).getNgayTT()), hoaDon.get(i).getTTGiamGia(), a - hoaDon.get(i).getTTGiamGia()});
            }
        }
    }

    private void loadTablehoaDonCT(List<HoaDonCT> hoaDonCT) {
        _Model = new DefaultTableModel();
        _Model = (DefaultTableModel) tbl_DonhangCT.getModel();
        _Model.setRowCount(0);
        long tien = 0;
        for (int i = 0; i < hoaDonCT.size(); i++) {
            tien = tien + hoaDonCT.get(i).getSoLuong() * hoaDonCT.get(i).getSanPham().getGiaBan();
            _Model.addRow(new Object[]{hoaDonCT.get(i).getSanPham().getTen(), hoaDonCT.get(i).getSoLuong(), hoaDonCT.get(i).getSoLuong() * hoaDonCT.get(i).getGiaLM()});
        }
    }

