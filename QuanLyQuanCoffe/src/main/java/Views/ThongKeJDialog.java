    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Date dateBD = null;
        Date dateKT = null;
        try {
            String dateB = txtNgay.getSelectedItem() + "/" + txtThang.getSelectedItem() + "/" + txtNam.getSelectedItem();
            dateBD = (Date) dateFormat.parse(dateB);
            String dateK = txtNgay1.getSelectedItem() + "/" + txtThang1.getSelectedItem() + "/" + txtNam1.getSelectedItem();
            dateKT = (Date) dateFormat.parse(dateK);
        } catch (ParseException e) {
            throw new RuntimeException(e);
        }
        hoaDons.clear();
        hoaDons = new ArrayList<>();
        List<HoaDon> hoaDonList = hoaDonService.getList();
        int T1 = 0;
        int TG = 0;
        for (int i = 0; i < hoaDonList.size(); i++) {
            if (hoaDonList.get(i).isTinhTrangHD()) {
                if (dateBD.compareTo(hoaDonList.get(i).getNgayTT()) <= 0 && dateKT.compareTo(hoaDonList.get(i).getNgayTT()) >= 0) {
                    TG = TG + hoaDonList.get(i).getTTGiamGia();
                    hoaDons.add(hoaDonList.get(i));
                    List<HoaDonCT> hoaDonCT = hoaDonCTService.getList(hoaDonService.getById(hoaDonList.get(i).getId()));
                    
                    for (int j = 0; j < hoaDonCT.size(); j++) {

                        T1 = T1 + (hoaDonCT.get(j).getGiaLM() * hoaDonCT.get(j).getSoLuong());
                    }
                }
            }
        }
        txtTien1.setText(String.valueOf(T1));
        txtTienGG.setText(String.valueOf(TG));
        txtTienTT.setText(String.valueOf(T1-TG));
        loadTablehoaDon();
    }//GEN-LAST:event_jButton3ActionPerformed

