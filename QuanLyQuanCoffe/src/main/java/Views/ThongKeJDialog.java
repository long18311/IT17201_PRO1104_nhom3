    private void loadTablehoaDon() {
        _Model = new DefaultTableModel();
        _Model = (DefaultTableModel) tbl_Donhang.getModel();
        _Model.setRowCount(0);
        for (int i = 0; i < hoaDons.size(); i++) {
            int a = 0;
            hoaDonCTs = new ArrayList<>();
            hoaDonCTs = hoaDonCTService.getList(hoaDons.get(i));
            for (int j = 0; j < hoaDonCTs.size(); j++) {
                a = a + hoaDonCTs.get(j).getGiaLM()*hoaDonCTs.get(j).getSoLuong();
            }
            _Model.addRow(new Object[]{hoaDons.get(i).getKhachHang().getTenKH(), "Đã thanh toán", new SimpleDateFormat("dd/MM/yyyy").format(hoaDons.get(i).getNgayTT()), hoaDons.get(i).getTTGiamGia(), a - hoaDons.get(i).getTTGiamGia()});

        }
    }

    private void loadTablehoaDonCT(List<HoaDonCT> hoaDonCT) {
        _Model = new DefaultTableModel();
        _Model = (DefaultTableModel) tbl_DonhangCT.getModel();
        _Model.setRowCount(0);
        long tien = 0;
        for (int i = 0; i < hoaDonCT.size(); i++) {
            tien = tien + hoaDonCT.get(i).getSoLuong() * hoaDonCT.get(i).getSanPham().getGiaBan();
            _Model.addRow(new Object[]{hoaDonCT.get(i).getSanPham().getTen(), hoaDonCT.get(i).getSoLuong(), hoaDonCT.get(i).getSoLuong() * hoaDonCT.get(i).getGiaLM(), hoaDonCT.get(i).getGiaLM()});
        }
    }

